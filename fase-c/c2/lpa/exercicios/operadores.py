# Escreva as seguintes expressões algébricas em linguagem Python:

# A) O somatório dos 5 primeiros números inteiros e positivos
print(1 + 2 + 3 + 4 + 5)

# A média entre 23, 19 e 31
print((23 + 19 + 31) / 3)

# O número de vezes que 73 cabe em 403
print(403 // 73)    # // divisão (somente a parte inteira)

# A sobra quando 403 é dividido por 73
print(403 % 73)  #Modúlo (resto da divisão)

# 2 elevado á 10ª potência
print(2 ** 10)

# O valor absoluto da diferença entre 54 e 57
print(abs(54 - 57)) #abs retorna valor absoluto

# O menor valor entre 34, 29 e 31
print(min(34, 29, 31))
